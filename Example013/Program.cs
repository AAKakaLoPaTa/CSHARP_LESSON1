void PrintArray(int[,] matr)
{

    for (int rows = 0; rows < matr.GetLength(0); rows++)
    {
        for (int cols = 0; cols < matr.GetLength(1); cols++)
        {
            Console.Write($"{matr[rows, cols]} ");
        }
        Console.WriteLine();
    }
}
void PrintImage(int[,] matr)
{

    for (int rows = 0; rows < matr.GetLength(0); rows++)
    {
        for (int cols = 0; cols < matr.GetLength(1); cols++)
        {
            //Console.Write($"{matr[rows, cols]} ");
            if (matr[rows, cols] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}
void FillArray(int[,] matr)
{
    for (int rows = 0; rows < matr.GetLength(0); rows++)
    {
        for (int cols = 0; cols < matr.GetLength(1); cols++)
        {
            matr[rows, cols] = new Random().Next(1, 10); // [1,10)
        }
    }
}


